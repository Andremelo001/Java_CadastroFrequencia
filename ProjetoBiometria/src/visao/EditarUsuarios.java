    private void carregarFoto(){
      CadastrarDAO objcadastrardao = new CadastrarDAO();
        ResultSet rs = objcadastrardao.carrCampos(matriculaaluno);

        try {
            while (rs.next()) {
                ImageIcon imageIcon = new ImageIcon(rs.getString("foto_aluno"));

                Image image = imageIcon.getImage(); // transform it 
                Image newimg = image.getScaledInstance(120, 140, java.awt.Image.SCALE_SMOOTH); // scale it the smooth way 
                /* Fim do código do redimensionamento */

                ImageIcon icon = new ImageIcon(newimg);
          
                moldura.setIcon(icon);
            }
        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    

