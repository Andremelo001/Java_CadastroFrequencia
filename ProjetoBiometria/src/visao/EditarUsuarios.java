    private void AlterarAlunos() {
        int id_Aluno;
        String aluno, matricula, turma;

        id_Aluno = Integer.parseInt(txtIdAlunoEdit.getText());
        aluno = txtNomeAlunoEdit.getText();
        matricula = txtMatriculaAlunoEdit.getText();
        turma = txtTurmaAlunoEdit.getText();

        CadastroAlunos objcadastroalunos = new CadastroAlunos();
        objcadastroalunos.setId_aluno(id_Aluno);
        objcadastroalunos.setAluno(aluno);
        objcadastroalunos.setMatricula(matricula);
        objcadastroalunos.setTurma(turma);

        CadastrarDAO objcacadastrardao = new CadastrarDAO();
        objcacadastrardao.alterarAlunos(objcadastroalunos);

        JOptionPane.showMessageDialog(null, "Cadastro editado com sucesso");
    }

    
    private void ExcluirAlunos(){
        int id_Aluno;
        
        id_Aluno = Integer.parseInt(txtIdAlunoEdit.getText());
        
        CadastroAlunos objcadastroalunos = new CadastroAlunos();
        objcadastroalunos.setId_aluno(id_Aluno);
        
        CadastrarDAO objcadastrardao = new CadastrarDAO();
        objcadastrardao.excluirAlunos(objcadastroalunos);
        
         JOptionPane.showMessageDialog(null, "Cadastro Excluido com sucesso");
    }

